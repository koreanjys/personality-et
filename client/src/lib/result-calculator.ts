export function calculatePersonalityType(answers: Answer[]): string {
  const scores = {
    E: 0, I: 0,
    S: 0, N: 0,
    T: 0, F: 0,
    J: 0, P: 0,
    에겐: 0, 테토: 0
  };

  answers.forEach(answer => {
    const question = questions.find(q => q.id === answer.questionId);
    if (!question) return;

    if (question.type === "EI") {
      if (answer.selectedOption === "A") scores.E++;
      else scores.I++;
    } else if (question.type === "SN") {
      if (answer.selectedOption === "A") scores.S++;
      else scores.N++;
    } else if (question.type === "TF") {
      if (answer.selectedOption === "A") scores.T++;
      else scores.F++;
    } else if (question.type === "JP") {
      if (answer.selectedOption === "A") scores.J++;
      else scores.P++;
    } else if (question.type === "ET") {
      if (answer.selectedOption === "A") scores.에겐++;
      else scores.테토++;
    }
  });

  const mbti = 
    (scores.E > scores.I ? "E" : "I") +
    (scores.S > scores.N ? "S" : "N") +
    (scores.T > scores.F ? "T" : "F") +
    (scores.J > scores.P ? "J" : "P");
  
  const egenTeto = scores.에겐 > scores.테토 ? "에겐" : "테토";
  
  return `${mbti} + ${egenTeto}`;
}

